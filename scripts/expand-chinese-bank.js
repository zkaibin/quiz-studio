function normalize(text) {
  return String(text || '').replace(/\s+/g, ' ').trim();
}

function hasCharacterPlaceholder(text) {
  return /\{CHARACTER_\d+\}/.test(String(text || ''));
}

function getHighestCharacterIndex(question) {
  const combinedText = [question.template, ...(question.options || [])].join(' ');
  const matches = [...combinedText.matchAll(/\{CHARACTER_(\d+)\}/g)];
  return matches.length > 0
    ? Math.max(...matches.map(match => Number(match[1])))
    : -1;
}

function ensureCharacterContext(question) {
  const roleDefaults = ['protagonist', 'helper', 'friend', 'classmate'];
  const prefixByCategory = {
    '汉语拼音': '{CHARACTER_0}在学拼音，',
    '词汇': '{CHARACTER_0}在学词语，',
    '成语': '{CHARACTER_0}在学成语，',
    '语法': '{CHARACTER_0}在练习语法，',
    '标点符号': '{CHARACTER_0}在写句子，',
    '阅读理解': '{CHARACTER_0}正在做阅读理解题。',
    '造句与改写': '{CHARACTER_0}在练习造句，',
    '汉字与字义': '{CHARACTER_0}在学汉字，'
  };

  if (!hasCharacterPlaceholder(question.template)) {
    const prefix = prefixByCategory[question.category] || '{CHARACTER_0}在练习华文，';
    question.template = `${prefix}${question.template}`;
  }

  const highestIndex = getHighestCharacterIndex(question);
  const neededRoles = Math.max(highestIndex + 1, 1);
  const roles = Array.isArray(question.placeholder_roles) ? [...question.placeholder_roles] : [];

  while (roles.length < neededRoles) {
    roles.push(roleDefaults[roles.length] || 'protagonist');
  }

  question.placeholder_roles = roles.map((role, index) => role || roleDefaults[index] || 'protagonist');
  return question;
}

questions.forEach(q => ensureCharacterContext(q));

let nextId = Math.max(...questions.map(q => Number(String(q.id).replace(/\D/g, '')))) + 1;
const existingTemplates = new Set(questions.map(q => normalize(q.template)));

function makeId() {
  return `CHN${String(nextId++).padStart(3, '0')}`;
}

function sampleWrongs(correct, pool, startIndex = 0) {
  const filtered = pool.filter(item => item !== correct);
  const picked = [];
  for (let i = 0; picked.length < 3 && i < filtered.length; i++) {
    const item = filtered[(startIndex + i) % filtered.length];
    if (!picked.includes(item)) picked.push(item);
  }
  return picked;
}

function addQuestion(question) {
  question = ensureCharacterContext({ ...question });
  const template = normalize(question.template);
  if (existingTemplates.has(template)) return false;
  if (!Array.isArray(question.options) || question.options.length !== 4) {
    throw new Error(`Question has invalid option count: ${template}`);
  }
  if (new Set(question.options).size !== 4) {
    throw new Error(`Question has duplicate options: ${template}`);
  }

  const newQuestion = {
    id: makeId(),
    category: question.category,
    difficulty: question.difficulty,
    template: question.template.trim(),
    placeholder_roles: question.placeholder_roles || [],
    options: question.options,
    answer: question.answer,
    correct_answer: question.options[question.answer]
  };

  questions.push(newQuestion);
  existingTemplates.add(template);
  return true;
}

function countCategory(category) {
  return questions.filter(q => q.category === category).length;
}

function ensureMinimum(category, minimum, candidates) {
  let current = countCategory(category);
  const needed = Math.max(0, minimum - current);
  let added = 0;

  for (const candidate of candidates) {
    if (current + added >= minimum) break;
    if (addQuestion(candidate)) added += 1;
  }

  const finalCount = countCategory(category);
  if (finalCount < minimum) {
    throw new Error(`${category} still has only ${finalCount} questions; minimum is ${minimum}.`);
  }

  return { category, before: current, added, after: finalCount };
}

